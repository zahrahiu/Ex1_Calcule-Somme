package org.example.calculesomme.Entity;

public class Nombres {
    private double a;
    private double b;

    public Nombres() {
    }

    public Nombres(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }
}
